public class medium {
    public static void main(String[] args) {
        
        for (int i = 0; i < 5; i ++){
            for (int k = 1; k < i + 2; k ++){
                System.out.print("*");
            }
            System.out.print("\n");
            System.out.print("\n");
            System.out.print("\n");
        }
    } 
    



    /*
    The answer to the Easy Java Control Flow challenge is:
    C. 20 -->
    k = 80 at the beginning 
    at the end, k = 80 - (12 * 5) = 20
    */

}
